    private void criarPadrao() {
        PadroesDAO padrDAO = new PadroesDAO();
        PadroesConfig padrConfig = new PadroesConfig();
        try {
            Padroes padr = new Padroes();
            padrConfig.setCodigoPadrao(Integer.parseInt(txtCodPadrao.getText()));
            padrConfig.setNomePadrao(txtNomePadrao.getText().toUpperCase());
            padrConfig.setCodigoSap(Integer.parseInt(txtCodSap.getText()));
            padrConfig.setObs(txtObs.getText().toUpperCase());
            padrConfig.setFormaFarmaceutica(txtFormaFarmaceutica.getText().toUpperCase());
            padrConfig.setCasNumber(txtCas.getText());
            padrConfig.setControleEspecial(ckbControleEspecial.isSelected());
            padrConfig.setListaControleEspecial(txtListaControleEspecial.getText());
            padr.setPadroesConfig(padrConfig);
            padrDAO.salvar(padr);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao gravar dados: " + e);
        }
    }

    private void atualizarPadrao() {
        PadroesConfigDAO padrConfigDAO = new PadroesConfigDAO();
        try {
            PadroesConfig padrConfig = padrConfigDAO.findById(PadroesConfig.class, padraoConfig_id);
            padrConfig.setCodigoPadrao(Integer.parseInt(txtCodPadrao.getText()));
            padrConfig.setNomePadrao(txtNomePadrao.getText().toUpperCase());
            padrConfig.setCodigoSap(Integer.parseInt(txtCodSap.getText()));
            padrConfig.setObs(txtObs.getText().toUpperCase());
            padrConfig.setFormaFarmaceutica(txtFormaFarmaceutica.getText().toUpperCase());
            padrConfig.setCasNumber(txtCas.getText());
            padrConfig.setControleEspecial(ckbControleEspecial.isSelected());
            if (ckbControleEspecial.isSelected()) {
                padrConfig.setListaControleEspecial(txtListaControleEspecial.getText());
            } else {
                padrConfig.setListaControleEspecial(null);
            }
            padrConfigDAO.salvar(padrConfig);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao gravar dados: " + e);
        }
    }

    private void carregarDados(Integer id) {
        try {
            PadroesConfigDAO padrConfigDAO = new PadroesConfigDAO();
            PadroesConfig padrConfig = padrConfigDAO.findById(PadroesConfig.class, id);
            txtCodPadrao.setText(String.valueOf(padrConfig.getCodigoPadrao()));
            txtNomePadrao.setText(padrConfig.getNomePadrao());
            txtCas.setText(padrConfig.getCasNumber());
            txtCodSap.setText(String.valueOf(padrConfig.getCodigoSap()));
            txtFormaFarmaceutica.setText(padrConfig.getFormaFarmaceutica());
            ckbControleEspecial.setSelected(padrConfig.getControleEspecial());
            txtListaControleEspecial.setEnabled(ckbControleEspecial.isSelected());
            txtListaControleEspecial.setText(String.valueOf(padrConfig.getListaControleEspecial()));
            txtObs.setText(padrConfig.getObs());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao gravar dados: " + e);
        }

    }
