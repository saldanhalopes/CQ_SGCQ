    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (txtCodPadrao.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Código do Padrão inválido");
            txtCodPadrao.requestFocus();
        } else if (txtNomePadrao.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nome do Padrão inválido");
            txtNomePadrao.requestFocus();
        } else if (padraoConfig_id != null) {
            setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
            FrmSalvar frmSalvar = new FrmSalvar(null, true);
            frmSalvar.setVisible(true);
            if (FrmSalvar.senha()) {
                atualizarPadrao();
                dispose();
            }
            setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
        } else {
            setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
            FrmSalvar frmSalvar = new FrmSalvar(null, true);
            frmSalvar.setVisible(true);
            if (FrmSalvar.senha()) {
                criarPadrao();
                dispose();
            }
            setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

